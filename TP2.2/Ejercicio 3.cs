//3. Dados dos números enteros distintos, mostrarlos ordenados de forma creciente

Console.WriteLine("Ingrese primer número");
int N1 = int.Parse(Console.ReadLine());

Console.WriteLine("Ingrese segundo número");
int N2 = int.Parse(Console.ReadLine());

if (N1 > N2) {
    Console.WriteLine("El primer número es mayor al segundo");
    Console.WriteLine("El orden creciente sería: " +  N2 + " " + N1);
}

else
{
    Console.WriteLine("El segundo número es mayor al primero");
    Console.WriteLine("El orden creciente sería: " + N1 + " " + N2);
}
