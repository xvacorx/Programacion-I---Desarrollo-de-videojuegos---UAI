//1. Ingresar la nota de un alumno correspondiente a un parcial (valor con decimales)
//e informar si está Aprobado o Reprobado. Aprobado: mayor o igual a 4.

Console.WriteLine("Ingrese nota del alumno");
float Nota = float.Parse(Console.ReadLine());

if (Nota >= 4) { Console.WriteLine("El alumno está aprobado"); }
else { Console.WriteLine("El alumno está desaprobado"); }
