//3. Calcular el área y el volumen de un paralelepípedo de base rectangular.

Console.WriteLine("Ingrese medida de base: ");
float Base = float.Parse(Console.ReadLine());

Console.WriteLine("Ingresar medida de altura: ");
float Altura = float.Parse(Console.ReadLine());

Console.WriteLine("Ingresar medida de profundidad: ");
float Profundidad = float.Parse(Console.ReadLine());


float Area = 2*(Base * Altura + Altura * Profundidad + Base * Profundidad);

float volumen = Base * Altura * Profundidad;

Console.WriteLine("El área es de: " + Area);
Console.WriteLine("El volumen es de: " + volumen);
