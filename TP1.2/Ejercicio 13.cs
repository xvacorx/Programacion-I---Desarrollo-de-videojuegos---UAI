//13.Dado un ángulo en decimal expresarlo en sexagesimal.

Console.Write("Ingrese el ángulo en decimal: ");
double angulo = double.Parse(Console.ReadLine());

int Grados = (int)angulo;
int Minutos = (int)((angulo - Grados) * 60);
int Segundos = (int)(((angulo - Grados) * 60 - Minutos) * 60);

Console.WriteLine($"El ángulo en sexagesimal es: {Grados}° {Minutos}' {Segundos}''");
