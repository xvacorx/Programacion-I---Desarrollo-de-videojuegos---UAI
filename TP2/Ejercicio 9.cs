//9. Ingresar el sueldo, categoría y antigüedad de un empleado,
//calcular el sueldo final si el empleado es de la categoría 1
//se le adiciona $50 por cada año de antigüedad. Si es categoría 2 o mayor , no se le adiciona nada.

Console.WriteLine("Ingrese sueldo del empleado:");
int Sueldo = int.Parse(Console.ReadLine());

Console.WriteLine("Ingrese categoría del empleado:");
int Categoria = int.Parse(Console.ReadLine());

Console.WriteLine("Ingrese antiguedad del empleado:");
int Antiguedad  = int.Parse(Console.ReadLine());

if (Categoria == 1) {
    Sueldo += Antiguedad * 50;
}

Console.WriteLine("El sueldo total es de: " + Sueldo);
